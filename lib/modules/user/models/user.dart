class User {
  String fullname;
  String email;
  String phone;
  String avatar;
  String gender;
  DateTime birthDay;
  String address;
  String bankName;
  String bankAccount;
  String momo;
  bool isEnabled;

  User({
    required this.fullname,
    required this.email,
    required this.phone,
    required this.avatar,
    required this.gender,
    required this.birthDay,
    required this.address,
    required this.bankName,
    required this.bankAccount,
    required this.momo,
    required this.isEnabled,
  });
}
